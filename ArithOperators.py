# Arithmetic Operators

# Addition
addition=10+5
print(f"Addition (10+5):{addition}")
# Subtraction
subtraction=10-5
print(f"Subtraction (10-5):{subtraction}")
# Multiplication
multiplicaton=10*5
print(f"Multiplicaton (10*5):{multiplicaton}")
# Division
division=10/5
print(f"Division (10/5):{division}")
# Modulus
modulus=10%3
print(f"Modulus (10%3):{modulus}")
# Exponentiation
exponentiation=2**3
print(f"Exponentiation (2**3):{exponentiation}")
# Floor Division
floor_division=10//3
print(f"Floor Division (10//3):{floor_division}")