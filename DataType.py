# Integer Numbers
a=5
print(a,type(a))
# FLoating Numbers
b=5.0
print(b,type(b))
# Complex Numbers
c=2+4j
print(c,type(c))
# List with Int Values
lst=[1,2,3]
print(lst,type(lst))
# Tuple with Int Values
tpl=(1,2,3)
print(tpl,type(tpl))
# Set with Int Values
st={1,2,3}
print(st,type(st))
# Dictionary with Int Values
dct={'22154-cm-001':'suresh','22154-cm-002':'ramesh','22154-cm-003':'havesh'}
